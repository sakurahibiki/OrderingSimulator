    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String key = this.jTextField1.getText();

        if (key == null || key.isEmpty()) {
            JOptionPane.showMessageDialog(this, "This field cannot be empty!");

            return;

        }
        

        if (OrderLoader.orders.contains(key) == true) {

            JOptionPane.showMessageDialog(this, "This Order ID already exists.");
            

            return;

        }

        this.jTextField2.setText(key);
        
    }//GEN-LAST:event_jButton2ActionPerformed

