    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String key = this.jTextField2.getText();
        
        if (key == null || key.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Verify the record first.");
            return;
        }
        
        Order data = new Order();
        
        data.setOrderId(this.jTextField2.getText());
        data.setName(this.jTextField3.getText());
        data.setStyle(this.jTextField4.getText());
        
        
        OrderLoader.orders.insert(key, data);
        JOptionPane.showMessageDialog(this, "Record (" + key + ") was successfully inserted!");
        
        
        this.jTextField1.setText("");
        this.jTextField2.setText("");
        this.jTextField3.setText("");
        this.jTextField4.setText("");
        
    }//GEN-LAST:event_jButton1ActionPerformed

